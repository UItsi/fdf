#include "fdf.h"

int	colour(t_struct a, int i0, int i1)
{
	if (a.arr[i1][i0] == a.min)
		return (0x44D62C);
	if (a.arr[i1][i0] == a.max)
		return (0xE4002B);
	return (0xFFCD00);
}
